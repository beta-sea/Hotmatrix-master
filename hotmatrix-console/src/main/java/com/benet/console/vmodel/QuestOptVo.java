package com.benet.console.vmodel;

public class QuestOptVo {

    private String optNo;
    private String optTitle;
    private String optDesc;
    private String optScore;

    public String getOptNo() {
        return optNo;
    }

    public void setOptNo(String optNo) {
        this.optNo = optNo;
    }

    public String getOptTitle() {
        return optTitle;
    }

    public void setOptTitle(String optTitle) {
        this.optTitle = optTitle;
    }

    public String getOptDesc() {
        return optDesc;
    }

    public void setOptDesc(String optDesc) {
        this.optDesc = optDesc;
    }

    public String getOptScore() {
        return optScore;
    }

    public void setOptScore(String optScore) {
        this.optScore = optScore;
    }
}
